const month=3
switch (month) {
    case 1:
        console.log("January")
        break;
    case 2:
        console.log("february")
        break;
    case 3:
        console.log("March")
        break;
    case 4:
        console.log("April")
        break;

    default:
        console.log("Default case match")
        break;
}

const m="march"
switch (m) {
    case "jan":
        console.log("January")
        break;
    case "feb":
        console.log("february")
        break;
    case "march":
        console.log("March")
        break;

    default:
        console.log("Default case")
        break;
}
